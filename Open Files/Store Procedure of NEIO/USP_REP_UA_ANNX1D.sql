Declare @MultiCo	VarChar(3)

Begin ------Fetch Single Co. Data
	 Set @MultiCo = 'NO'
	 EXECUTE USP_REP_SINGLE_CO_DATA_VAT
	  @TMPAC, @TMPIT, @SPLCOND, @SDATE, @EDATE
	 ,@SAC, @EAC, @SIT, @EIT, @SAMT, @EAMT
	 ,@SDEPT, @EDEPT, @SCATE, @ECATE,@SWARE
	 ,@EWARE, @SINV_SR, @EINV_SR, @LYN, 'PT'
	 ,@MFCON = @MCON OUTPUT

	 SELECT ISNULL(I.IT_NAME,'') AS ITEM, ISNULL(SUM(A.Gro_amt),0) as AMT1, LEVEL1=ISNULL(STM.LEVEL1,0),
	 VALIDAMT = ISNULL(SUM(A.VATONAMT),0) FROM VATTBL A INNER JOIN IT_MAST I ON (I.IT_CODE = A.IT_CODE)
	 LEFT JOIN STAX_MAS STM ON (STM.TAX_NAME = A.TAX_NAME AND STM.ENTRY_TY = A.BHENT) WHERE A.BHENT in ('PT') AND (A.DATE BETWEEN @SDATE AND @EDATE)
	 AND A.S_TAX <> ' ' AND A.TaxAmt > 0 AND A.U_IMPORM IN ('Under Section 3(10)','u/s 3(10)') AND A.St_type = 'LOCAL' AND A.Tax_name like '%VAT%'
	 GROUP BY I.IT_NAME,LEVEL1 ORDER BY I.IT_NAME,LEVEL1
	
End

END
